#引数x, y, zを受け取り「x時のyはz」という文字列を返す関数を実装せよ．さらに，x=12, y="気温", z=22.4として，実行結果を確認せよ．#
###############################################################################################################

# format関数：https://docs.python.jp/3/library/string.html#custom-string-formatting

# coding:utf-8


def format(x,y,z):
    return '{hour}時の{target}は{value}'.format(hour = x,target = y,value = z)

x = 12
y = '気温'
z = 22.4

print(format(x,y,z))